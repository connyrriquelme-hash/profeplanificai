function mapToItems(
  nivel: string,
  asignatura: string,
  entries: {
    eje: string; id: string; oa: string; habilidad: string;
    indicadores: string[]; conoc: string[]; act: string[];
    kw: string[]; acts: string[]; evals: string[]; rec: string[];
  }[],
): CurriculumItem[] {
  return entries.map((e) => ({
    id: e.id,
    fuente: 'ejemplo_editable' as const,
    nivel: nivel as CurriculumItem['nivel'],
    curso: nivel === 'Educación Media' ? '1° medio' : '2° básico',
    asignatura,
    eje: e.eje,
    oa: e.oa,
    habilidad: e.habilidad,
    indicadores: e.indicadores,
    conocimientos: e.conoc,
    actitudes: e.act,
    palabrasClave: e.kw,
    actividadesSugeridas: e.acts,
    evaluacionesSugeridas: e.evals,
    recursos: e.rec,
  }));
}

